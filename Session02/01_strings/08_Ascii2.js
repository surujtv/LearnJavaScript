// String.fromCharCode(ASCII_Value) --> this method returns character based on given ASCII values-->

let char = String.fromCharCode(65)
console.log(char)   // Output-- A (because 65 is ASCII value of A)

//===================================================================

// we can pass multiple ascii values as an argument to get characters-->

let charaters = String.fromCharCode(83,97,110,100,104,121,97)
console.log(charaters)  // output-- Sandhya