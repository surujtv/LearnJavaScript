// arr_name.indexOf(element) --> returns index value of given element if it's present in the array

let arr = [10,20,30, 'Surendra', 'Jatav']

// if element present in the array-->
console.log(arr.indexOf('Surendra'));   // output --> 3

// if element not present in the array
console.log(arr.indexOf('Sandhya'));   // output (-1)
